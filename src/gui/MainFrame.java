    private void btnTslActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTslActionPerformed
        String inputWord = txtWord.getText();
        //LinkedList<String> listVocab = null;
        if(inputWord.isEmpty()){
            return;
        }
        if(rdEV.isSelected()){
            listVocab = d.translateEnToVi(inputWord);
            if(listVocab != null){
                txtWtsl.setText(listVocab.get(0));
                txtAreaSnm.setText(listVocab.toString());
                Iterator i = listVocab.iterator();
                while (i.hasNext()) {
                    txtAreaSnm.setToolTipText((String) i.next());
                }
            }else{
                JOptionPane.showMessageDialog(null, "This word is not in the dictionary.");
            }           
        }else{
            listVocab = d.translateViToEn(inputWord);
            if(listVocab != null){
                txtWtsl.setText(listVocab.get(0));
                txtAreaSnm.setText(listVocab.toString());
                Iterator i = listVocab.iterator();
                while (i.hasNext()) {
                    txtAreaSnm.setToolTipText((String) i.next());
                }
            }else{
                JOptionPane.showMessageDialog(null, "Từ này không có trong từ điển.");
            }      
        }
        
    }//GEN-LAST:event_btnTslActionPerformed

