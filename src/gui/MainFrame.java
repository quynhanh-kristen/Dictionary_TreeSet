    private void btnAddWordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddWordActionPerformed
        String newWord = txtWord.getText();
           String mean = txtWtsl.getText();
           
        if(rdEV.isSelected()){           
           if(!newWord.isEmpty() && !mean.isEmpty()){
               if(d.addEnWord(newWord, mean)){
                   d.addEVToVn_En(newWord, mean);
                   loadData();
                   JOptionPane.showMessageDialog(null, "Done!!!");
               }
           }
        }else{
           if(!newWord.isEmpty() && !mean.isEmpty()){
               if(d.addVnWord(newWord, mean)){
                   d.addVEToEn_Vn(newWord, mean);
                   loadData();
                   JOptionPane.showMessageDialog(null, "Đã xong!!!");
               }else{
                   JOptionPane.showMessageDialog(null, "Từ này đã có trong từ điển.");
               }
           } 
        }
    }//GEN-LAST:event_btnAddWordActionPerformed

